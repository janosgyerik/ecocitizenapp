package com.ecocitizen.common.parser;

public class SensorData {

	String unit;
	String name;
	float floatValue;
	String strValue;
	int level;
	
	public String getUnit() {
		return unit;
	}

	public String getName() {
		return name;
	}
	
	public float getFloatValue() {
		return floatValue;
	}

	public String getStrValue() {
		return strValue;
	}

	public int getLevel() {
		return level;
	}

}
